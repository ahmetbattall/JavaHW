package class7;

public class E9WhileLoop {
    public static void main(String[] args) {

        // print 25 20  10 5 using while loop

        int counter = 25;
        while (counter >4) {
            if (counter != 15) // it is false so it will not print 15, because it is actually counter = 15 at one point

            {
                System.out.print(counter + " ");

            }
            counter -= 5;
        }













    }
}
