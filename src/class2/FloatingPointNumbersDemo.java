package class2;

public class FloatingPointNumbersDemo {

    public static void main(String[] args) {
       /*
        by default all the floating point numbers are doubles thats why we need to write f/F at the and of the
        float type veriables but we dont need to worry about it as %99 of the time we will be using a double
        */
        float grocery = 20.5f; // smaller size data type
        System.out.println(grocery);

        double price = 11111.222222111444555; // bigger size data type, can hold double amount of float
        System.out.println(price);





    }






}
